package core.lectures.basics

object ValuesVariablesTypes extends App {

  //VALS ARE IMMUTABLE
  val x: Int = 43
  val y = 77
  println(x)
  println(y)

  val aString: String = "hello, man"
  val aBoolean: Boolean = true
  val aChar: Char = 'f'
  val anInt: Int = x
  val aShort: Short = 4613
  val aLong: Long = 4842113131687L
  val aFloat: Float = 0.15f
  val aDouble: Double = 0.15

  //variables
  var aVariable: Int = 71
  aVariable = 17


}
